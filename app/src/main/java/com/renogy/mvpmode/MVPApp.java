public class MVPApp extends MultiDexApplication {

    private static MVPApp INSTANCE;

    public static MVPApp getInstance() {
        return INSTANCE;
    }

    @Override
    public void onCreate() {
        super.onCreate();
        INSTANCE = this;
        init();
    }

    private void init() {
        ServiceHelper.getInstance().init();
        //本地数据库
        LitePal.initialize(INSTANCE);
        //log日志
        LogUtils.getConfig().setBorderSwitch(true).setLogSwitch(true).setGlobalTag(AppConstants.APP_DEFAULT_LOG);
        //轻量化存储
        MkvEditorUtils.getInstance().init();
    }
}
